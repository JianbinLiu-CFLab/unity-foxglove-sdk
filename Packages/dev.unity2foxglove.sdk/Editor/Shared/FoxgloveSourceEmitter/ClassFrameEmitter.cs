using System.Text;

namespace Unity.FoxgloveSDK.Editor
{
    /// <summary>
    /// Emits the class declaration frame, using directives, and interface
    /// implementation boilerplate for a generated FoxRun partial class.
    /// </summary>
    internal static class ClassFrameEmitter
    {
        /// <summary>
        /// Emits the opening class declaration, using directives,
        /// <c>[Preserve]</c> attribute, and interface implementation line for
        /// a generated FoxRun partial class. Returns the indentation pad string
        /// for subsequent method bodies.
        /// </summary>
        internal static string EmitClassFrame(StringBuilder sb, string ns, string className, int topicCount, bool hasPolicy, string pad)
        {
            sb.AppendLine("// <auto-generated/>");
            sb.AppendLine("// Copyright (c) 2026 Jianbin Liu and Unity2Foxglove contributors.");
            sb.AppendLine("// SPDX-License-Identifier: Apache-2.0");
            sb.AppendLine("// Generated by the Unity2Foxglove [FoxRun] source emitter.");
            sb.AppendLine("#pragma warning disable");
            sb.AppendLine("using System;");
            sb.AppendLine("using System.Collections.Generic;");
            sb.AppendLine("using UnityEngine;");
            sb.AppendLine("using UnityEngine.Scripting;");
            sb.AppendLine("using Unity.FoxgloveSDK.Components;");
            sb.AppendLine();

            if (!string.IsNullOrEmpty(ns)) { sb.AppendLine($"namespace {ns}"); sb.AppendLine("{"); }

            sb.AppendLine($"{pad}[Preserve]");
            sb.Append(hasPolicy
                ? $"{pad}partial class {className} : IFoxgloveLogSource, IFoxgloveLogPolicySource\n"
                : $"{pad}partial class {className} : IFoxgloveLogSource\n");
            sb.AppendLine($"{pad}{{");
            sb.AppendLine($"{pad}    int IFoxgloveLogSource.FoxgloveLog_TopicCount => {topicCount};");
            sb.AppendLine();

            return pad;
        }
    }
}
